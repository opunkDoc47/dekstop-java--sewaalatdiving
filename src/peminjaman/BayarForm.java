    private void tambahtabel() {
        boolean ada = false;
        int idpelanggan = 0;
        Pelanggan pelanggan = new Pelanggan();
        pelanggan.setNama(PeminjamanForm.textFieldNama.getText());
        pelanggan.setAlamat(PeminjamanForm.textFieldAlamat.getText());
        pelanggan.setNik(PeminjamanForm.textFieldNik.getText());
        pelanggan.setNohp(PeminjamanForm.textFieldNoHp.getText());
        if (dao.listPelanggan().isEmpty()) {
            dao.insertpelanggan(pelanggan);
        } else {
            for (Pelanggan p : dao.listPelanggan()) {
                if (p.getNik().equals(PeminjamanForm.textFieldNik.getText())) {
                    idpelanggan = p.getIdpelanggan();
                    ada = true;
                    break;
                }
            }
            if (ada == true) {
                pelanggan.setIdpelanggan(idpelanggan);
            } else {
                dao.insertpelanggan(pelanggan);
            }
        }

        User user = new User();
        user.setIduser(Login.ID);
        Peminjaman peminjam = new Peminjaman();
        peminjam.setUser_iduser(user);
        peminjam.setPelanggan_idpelanggan(pelanggan);
        peminjam.setStatuspinjaman("belumkembali");
        peminjam.setTotal(Double.valueOf(labelTotal.getText()));
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date();
        peminjam.setTanggal(dateFormat.format(date));
        dao.insertpeminjaman(peminjam);
        tomblBayar.setEnabled(false);
        tombolBatal.setEnabled(false);
    }

    private void reset() {
        transaksi = 0;
        PeminjamanIMP.listItem.clear();
        PeminjamanForm.tabelSewa.setModel(dao.viewItem());
        PeminjamanForm.textFieldAlamat.setText("");
        PeminjamanForm.textFieldNama.setText("");
        PeminjamanForm.textFieldNik.setText("");
        PeminjamanForm.textFieldNoHp.setText("");
        PeminjamanForm.labelTotal.setText("");
    }
