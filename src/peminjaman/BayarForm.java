    private void tomblBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tomblBayarActionPerformed
        if (textFieldBayar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "input pembayaran terlebih dahulu");
        } else {
            if (Double.valueOf(textFieldBayar.getText()) >= Double.valueOf(labelTotal.getText())) {
                tambahtabel();
                JOptionPane.showMessageDialog(null, "transaksi berhasil");
                double kembalian = Double.valueOf(textFieldBayar.getText()) - Double.valueOf(labelTotal.getText());
                JOptionPane.showMessageDialog(null, "kembalian sebesar Rp." + kembalian);
                reset();
                transaksi = 1;
            } else {
                JOptionPane.showMessageDialog(null, "MAAF PEMBAYARAN KURANG");
            }
        }
    }//GEN-LAST:event_tomblBayarActionPerformed

